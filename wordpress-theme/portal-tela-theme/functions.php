// Theme setup
add_action('after_setup_theme', 'portal_tela_setup');

function portal_tela_setup()
{
    // Add language support
    load_theme_textdomain('portal-tela-theme', get_template_directory() . '/languages');

    // Add theme support
    add_theme_support('automatic-feed-links');
    add_theme_support('title-tag');
    add_theme_support('post-thumbnails');
    add_theme_support('html5', array('search-form', 'comment-form', 'comment-list', 'gallery', 'caption'));
    add_theme_support('custom-background');
    add_theme_support('custom-header');

    // Register navigation menus
    register_nav_menus(array(
        'primary' => __('Primary Menu', 'portal-tela-theme'),
        'secondary' => __('Secondary Menu', 'portal-tela-theme'),
        'footer' => __('Footer Menu', 'portal-tela-theme'),
    ));
}

// Enqueue styles and scripts
add_action('wp_enqueue_scripts', 'portal_tela_enqueue_assets');

function portal_tela_enqueue_assets()
{
    // Get asset manifest (generated by Vite)
    $assets_dir = get_template_directory() . '/assets';

    // Enqueue main style
    if (file_exists($assets_dir . '/css/main.css')) {
        wp_enqueue_style(
            'portal-tela-main',
            get_template_directory_uri() . '/assets/css/main.css',
            array(),
            filemtime($assets_dir . '/css/main.css')
        );
    }

    // Enqueue main script
    if (file_exists($assets_dir . '/js/main.js')) {
        wp_enqueue_script(
            'portal-tela-main',
            get_template_directory_uri() . '/assets/js/main.js',
            array(),
            filemtime($assets_dir . '/js/main.js'),
            true
        );
    }

    // Remove emoji styles and scripts
    remove_action('wp_head', 'print_emoji_detection_script', 7);
    remove_action('wp_print_styles', 'print_emoji_styles');

}

// Register sidebars/widgets
