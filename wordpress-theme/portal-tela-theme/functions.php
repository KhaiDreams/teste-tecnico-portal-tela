// Add custom body classes
add_filter('body_class', function ($classes) {
    if (is_home()) {
        $classes[] = 'is-home';
    }

    if (is_single()) {
        $classes[] = 'is-single-post';
    }

    return $classes;
});

// Content generator integration
add_filter('the_content', 'portal_tela_add_content_generator_meta');

function portal_tela_add_content_generator_meta($content)
{
    if (is_single() && !is_admin()) {
        $generator_id = get_post_meta(get_the_ID(), '_content_generator_id', true);
        $source_url = get_post_meta(get_the_ID(), '_content_generator_source_url', true);

        if ($generator_id || $source_url) {
            $notice = '<div class="content-generator-notice card mt-4 p-3"><small class="text-muted">';
            $notice .= '🤖 Este conteúdo foi gerado por IA';
            if ($source_url) {
                $notice .= ' baseado em <a href="' . esc_url($source_url) . '" target="_blank">fonte original</a>';
            }
            $notice .= '</small></div>';
            $content .= $notice;
        }
    }

    return $content;
}

