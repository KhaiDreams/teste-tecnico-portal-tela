function portal_tela_register_sidebars()
{
    register_sidebar(array(
        'name' => __('Primary Sidebar', 'portal-tela-theme'),
        'id' => 'primary-sidebar',
        'description' => __('Main sidebar for blog posts', 'portal-tela-theme'),
        'before_widget' => '<div id="%1$s" class="widget %2$s">',
        'after_widget' => '</div>',
        'before_title' => '<h3 class="widget-title">',
